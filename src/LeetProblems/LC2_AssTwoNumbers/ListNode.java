package LeetProblems.LC2_AssTwoNumbers;
//Definition for singlylinked list.

public class ListNode {

    int val;
    ListNode next;

    ListNode() {
    }
    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}

